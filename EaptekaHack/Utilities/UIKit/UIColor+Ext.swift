import UIKit

extension UIColor {
    
    @nonobjc class var grayBackground: UIColor {
        return UIColor(named: "GrayBackground")!
    }
    
    @nonobjc class var blueBackground: UIColor {
        return UIColor(named: "BlueBackground")!
    }
    
    @nonobjc class var bluePrimary: UIColor {
        return UIColor(named: "BluePrimary")!
    }
    
    @nonobjc class var bluePrimaryLight: UIColor {
        return UIColor(named: "BluePrimaryLight")!
    }
    
    @nonobjc class var whiteBackground: UIColor {
        return UIColor(named: "WhiteBackground")!
    }
    
    @nonobjc class var greenPrice: UIColor {
        return UIColor(named: "Price")!
    }

    @nonobjc class var grayText: UIColor {
        return UIColor(named: "GrayText")!
    }

    
    @nonobjc class var gradientColorTop: UIColor {
        return UIColor(named: "GradientColorTop")!
    }

    @nonobjc class var gradientColorBottom: UIColor {
        return UIColor(named: "GradientColorBottom")!
    }
    
}
